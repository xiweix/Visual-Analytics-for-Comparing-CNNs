import os
import cv2
import numpy as np
import torch
import PIL
import torch.nn.functional as F
from torchvision.utils import save_image


def visualize_cam(mask, img):
    """Make heatmap from mask and synthesize GradCAM result image using heatmap and img.
    Args:
        mask (torch.tensor): mask shape of (1, 1, H, W) and each element has value in range [0, 1]
        img (torch.tensor): img shape of (1, 3, H, W) and each pixel value is in range [0, 1]

    Return:
        heatmap (torch.tensor): heatmap img shape of (3, H, W)
        result (torch.tensor): synthesized GradCAM result of same shape with heatmap.
    """
    heatmap = cv2.applyColorMap(mask, cv2.COLORMAP_VIRIDIS)
    heatmap = torch.from_numpy(heatmap).permute(2, 0, 1).float().div(255)
    b, g, r = heatmap.split(1)
    heatmap = torch.cat([r, g, b])

    result = heatmap+img.cpu()
    result = result.div(result.max()).squeeze()

    return heatmap, result

def cam_to_img(dataset_path, image_name, model_name, output_path):
    img_path = os.path.join(dataset_path, 'ILSVRC2012_img_val', image_name)
    base_name = os.path.splitext(image_name)[0]
    output_dir = os.path.join(output_path, base_name)

    pil_img = PIL.Image.open(img_path).convert('RGB')
    torch_img = torch.from_numpy(np.asarray(pil_img)).permute(2, 0, 1).unsqueeze(0).float().div(255)
    torch_img = F.upsample(torch_img, size=(224, 224), mode='bilinear', align_corners=False)

    mask = np.load(os.path.join(output_dir, f'{model_name}_gradcam_mask.npy'))
    mask_pp = np.load(os.path.join(output_dir, f'{model_name}_gradcampp_mask.npy'))

    heatmap, result = visualize_cam(mask, torch_img)
    save_image(heatmap, os.path.join(output_dir, f'{model_name}_gradcam2.png'))
    save_image(result, os.path.join(output_dir, f'{model_name}_gradcam_img2.png'))

    heatmap_pp, result_pp = visualize_cam(mask_pp, torch_img)
    save_image(heatmap_pp, os.path.join(output_dir, f'{model_name}_gradcampp2.png'))
    save_image(result_pp, os.path.join(output_dir, f'{model_name}_gradcampp_img2.png'))


def find_resnet_layer(arch, target_layer_name):
    """Find resnet layer to calculate GradCAM and GradCAM++

    Args:
        arch: default torchvision resnet models
        target_layer_name (str): the name of layer with its hierarchical information. please refer to usages below.
            target_layer_name = 'conv1'
            target_layer_name = 'layer4'
            target_layer_name = 'layer4_1_conv1'
            target_layer_name = 'avgpool'
            target_layer_name = 'fc'

    Return:
        target_layer: found layer. this layer will be hooked to get forward/backward pass information.
    """
    if 'layer' in target_layer_name:
        hierarchy = target_layer_name.split('_')
        layer_num = int(hierarchy[0].lstrip('layer'))
        if layer_num == 1:
            target_layer = arch.layer1
        elif layer_num == 2:
            target_layer = arch.layer2
        elif layer_num == 3:
            target_layer = arch.layer3
        elif layer_num == 4:
            target_layer = arch.layer4
        else:
            raise ValueError('unknown layer : {}'.format(target_layer_name))

        if len(hierarchy) >= 2:
            target_layer = target_layer[int(hierarchy[1])]

        if len(hierarchy) >= 3:
            target_layer = target_layer._modules[hierarchy[2]]

        if len(hierarchy) == 4:
            target_layer = target_layer._modules[hierarchy[3]]

    else:
        target_layer = arch._modules[target_layer_name]

    return target_layer


